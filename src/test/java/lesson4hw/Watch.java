package lesson4hw;

public class Watch {
    private double lenght;
    private double width;
    private double weight;
    private String model;
    private String manufacturer;
    private String colour;
    private String type;
    private String watchband;
    private String kind;
    private String clockface;
    private int time;

    //---------------GETTERS/SETTERS---------------


    public double getLenght() {
        return lenght;
    }

    public void setLenght(double lenght) {
        this.lenght = lenght;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getManufacturer() {
        return manufacturer;
    }

    public void setManufacturer(String manufacturer) {
        this.manufacturer = manufacturer;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getWatchband() {
        return watchband;
    }

    public void setWatchband(String watchband) {
        this.watchband = watchband;
    }

    public String getKind() {
        return kind;
    }

    public void setKind(String kind) {
        this.kind = kind;
    }

    public String getClockface() {
        return clockface;
    }

    public void setClockface(String clockface) {
        this.clockface = clockface;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}
