package lesson4hw;

public class Skates {
    private double lenght;
    private double width;
    private double weight;
    private String model;
    private String colour;
    private String iceskates;
    private String inlineSkates;
    private String material;

    //---------------GETTERS/SETTERS---------------

    public double getLenght() {
        return lenght;
    }

    public void setLenght(double lenght) {
        this.lenght = lenght;
    }

    public double getWidth() {
        return width;
    }

    public void setWidth(double width) {
        this.width = width;
    }

    public double getWeight() {
        return weight;
    }

    public void setWeight(double weight) {
        this.weight = weight;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getColour() {
        return colour;
    }

    public void setColour(String colour) {
        this.colour = colour;
    }

    public String getIceskates() {
        return iceskates;
    }

    public void setIceskates(String iceskates) {
        this.iceskates = iceskates;
    }

    public String getInlineSkates() {
        return inlineSkates;
    }

    public void setInlineSkates(String inlineSkates) {
        this.inlineSkates = inlineSkates;
    }

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }
}
